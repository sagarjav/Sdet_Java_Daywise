package day05_Loops;

public class ContinueStatement {
	
	public static void main(String[] args) {
		//1.print only 3 5 8 from 1 to 10 numbers
		for(int i=1; i<=10; i++)  //initialization condition increment/Decrement
		
		{
			if(i==3 || i==5 || i==8) 
			{
			
				System.out.println(i);
				
				
			}
			
			
		}
	}
	
	

}
