package reverseString;

public class Star_Box {

	public static void main(String[] args) {
		
		//*****   //3 Rows 
		//*****    //5 column
		//*****
		
		//for Row
		for(int i=0; i<3; i++)
		{
			
			
			//for column
			for(int j=0; j<5; j++) 
			{
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
